
public class Quinto {

	public static void main(String[] args) {
		int a = 100;
		System.out.println(a + 50);
		byte b; 	// 1 byte
		short s; 	// 2 bytes
		int i; 		// 4 bytes
		long l; 	// 8 bytes
		
		float f;	// 4 bytes
		double d;	// 8 bytes
		
		int n1, n2;
		n1 = 10;
		n2 = 3;
		System.out.println(n1 / n2);
		
		float dn1, dn2;
		dn1 = n1;
		dn2 = n2;
		System.out.println(dn1/dn2);
	}

}
