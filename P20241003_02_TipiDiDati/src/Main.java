
public class Main {

	public static void main(String[] args) {
		/*
		 * Tipi di Dato: 
		 * 	Numerici: 
		 * 		Interi: 
		 * 			byte -> (8 bit) [-127/127] ;
		 * 			short -> (16 bit) [-32768 / 32768] ;
		 * 			int -> (32 bit) [-2147483648 / 2147483648] ;
		 * 			long -> (64 bit)
		 * 			char -> (16 bit) [0 / 65535] ;
		 * 		Virgola Mobile: 
		 * 			float -> (32 bit) ;
		 * 			double -> (64 bit) 
		 * Boolean: 
		 * 		True / False
		 */
		// Ctrl + Shift + F -> Formattazione Codice
		// Ctrl + Space -> Auto-Compilato
		byte b1 = 125;
		System.out.println(b1);
		b1 += 1;
		System.out.println(b1);

	}

}
